from collections import OrderedDict

print( 'Regular dictionary')
d = {}
d['a'] = 'A'
d['b'] = 'B'
d['c'] = 'C'
d['d'] = 'D'
d['e'] = 'E'

for k, v in d.items():
    print (k, v)

# This is the 2nd part
print ('\nOrderedDict:')
